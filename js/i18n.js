function getLang() {
  return localStorage.getItem("nezam-lang") || "en";
}

function setLang(lang) {
  localStorage.setItem("nezam-lang", lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  applyTranslations(lang);
  updateLangToggle(lang);
  if (typeof window.renderProducts === "function") {
    window.renderProducts();
  }
}

function applyTranslations(lang) {
  const t = translations[lang] || translations.en;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (t[key] !== undefined) {
      el.placeholder = t[key];
    }
  });
  document.title = document.querySelector("[data-i18n-title]")?.getAttribute(`data-i18n-title-${lang}`)
    || document.querySelector("[data-i18n-title]")?.getAttribute("data-i18n-title-en")
    || "Nezam Systems";
}

function updateLangToggle(lang) {
  document.querySelectorAll(".lang-toggle").forEach(btn => {
    btn.textContent = lang === "en" ? "عربي" : "EN";
    btn.setAttribute("aria-label", lang === "en" ? "Switch to Arabic" : "Switch to English");
  });
}

function toggleLang() {
  const current = getLang();
  setLang(current === "en" ? "ar" : "en");
}

document.addEventListener("DOMContentLoaded", () => {
  setLang(getLang());
});
